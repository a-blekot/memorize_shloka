package com.a_blekot.shlokas.common.utils

fun String.removeDiacritics() =
    replace("̄а̄", "a")
        .replace("̄ӣ", "и")
        .replace("̄ı̄", "і")
        .replace("̄ӯ", "у")
        .replace("̄р̣", "ри")
        .replace("̄р̣̄", "ри")
        .replace("̄л̣", "ли")
        .replace("̄ш́", "ш")
        .replace("̄м̇", "м")
        .replace("̄х̣", "х")
        .replace("̄н̇", "н")
        .replace("̄н̃", "н")
        .replace("̄н̣", "н")
        .replace("̄т̣", "т")
        .replace("̄д̣", "д")
        .replace("̄т̣", "т")
        .replace("̄д̣", "д")
        .replace("̄ā", "a")
        .replace("̄ī", "i")
        .replace("̄ū", "u")
        .replace("̄ṛ", "ree")
        .replace("̄ṝ", "ree")
        .replace("̄ḷ", "lee")
        .replace("̄ḹ", "lee")
        .replace("̄ś", "sh")
        .replace("̄ṣ", "sh")
        .replace("̄ṁ", "m")
        .replace("̄ḥ", "h")
        .replace("̄ṅ", "n")
        .replace("̄ñ", "n")
        .replace("̄ṇ", "n")
        .replace("̄ṭ", "t")
        .replace("̄ḍ", "d")
